export const sceneById = new Map(
  architectureScenes.map((scene) => [scene.id, scene]),
);

export function getScene(sceneId: string): ArchitectureScene {
  return sceneById.get(sceneId) ?? architectureScenes[0]!;
}

export function getSceneBreadcrumbs(sceneId: string) {
  const breadcrumbs: ArchitectureScene[] = [];
  let scene: ArchitectureScene | undefined = getScene(sceneId);

  while (scene) {
    breadcrumbs.unshift(scene);
    scene = scene.parentId ? sceneById.get(scene.parentId) : undefined;
  }

  return breadcrumbs;
}

export const searchableArchitecture: readonly SearchResult[] =
  architectureScenes.flatMap((scene) =>
    scene.nodes.map((item) => ({
      sceneId: scene.id,
      nodeId: item.id,
      sceneTitle: scene.title,
      nodeTitle: item.title,
      summary: item.summary,
    })),
  );
